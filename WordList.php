	 * Should only be called with a word generated by `word()`.
	 */
	public function soundsLike($word) {

		$bucket = $this->words[soundex($word)];
		$word = $bucket[mt_rand(0, sizeof($bucket)-1)];
		
		return ucfirst($word);
		
	}	
	
	public function __construct() {
		
		$snd = array();
		$this->words = array();
		
		// put into soundex categories
		foreach ($this->words() as $word) {
			
			$soundex = soundex($word);			
			
			if (!isset($this->words[$soundex]))
				$this->words[$soundex] = array();
			
			// put into soundex group
			array_push($this->words[$soundex], $word);
			$snd[$soundex] = 1;		
			
		}
		
		$this->snd = array_keys($snd);
		
	}	
	
